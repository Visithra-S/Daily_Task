class TernaryOperator{
	public static void main(String visi[]){
		int a=10,b=12;
		
		int c = (a<b) ? b : a;
		int d = (1<2) ? 2 : 1;
		boolean e = (a<b) ? true : false ;
		
		System.out.println("Value is : " + c);
		System.out.println("Value is :" + d);
		System.out.println("Value is :" + e);
	}
}
